package org.sang.bean;

public class Requirements {
    private String customer;
    private String department;
    private String jobName;
    private String jobType;
    private String jobResponsibility;
    private String jobDesc;
    private String degreeRequired;
    private String jobEmergency;
    private String jobPoints;
    private String quantityDemanded;
    private String workingPlace;
    private String interviewer;
    private String keyPoint;
    private String workOvertime;
    private String note;
    private Long id;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getCustomer() {
        return customer;
    }

    public void setCustomer(String customer) {
        this.customer = customer;
    }

    public String getDepartment() {
        return department;
    }

    public void setDepartment(String department) {
        this.department = department;
    }

    public String getJobName() {
        return jobName;
    }

    public void setJobName(String jobName) {
        this.jobName = jobName;
    }

    public String getJobType() {
        return jobType;
    }

    public void setJobType(String jobType) {
        this.jobType = jobType;
    }

    public String getJobResponsibility() {
        return jobResponsibility;
    }

    public void setJobResponsibility(String jobResponsibility) {
        this.jobResponsibility = jobResponsibility;
    }

    public String getJobDesc() {
        return jobDesc;
    }

    public void setJobDesc(String jobDesc) {
        this.jobDesc = jobDesc;
    }

    public String getDegreeRequired() {
        return degreeRequired;
    }

    public void setDegreeRequired(String degreeRequired) {
        this.degreeRequired = degreeRequired;
    }

    public String getJobEmergency() {
        return jobEmergency;
    }

    public void setJobEmergency(String jobEmergency) {
        this.jobEmergency = jobEmergency;
    }

    public String getJobPoints() {
        return jobPoints;
    }

    public void setJobPoints(String jobPoints) {
        this.jobPoints = jobPoints;
    }

    public String getQuantityDemanded() {
        return quantityDemanded;
    }

    public void setQuantityDemanded(String quantityDemanded) {
        this.quantityDemanded = quantityDemanded;
    }

    public String getWorkingPlace() {
        return workingPlace;
    }

    public void setWorkingPlace(String workingPlace) {
        this.workingPlace = workingPlace;
    }

    public String getInterviewer() {
        return interviewer;
    }

    public void setInterviewer(String interviewer) {
        this.interviewer = interviewer;
    }

    public String getKeyPoint() {
        return keyPoint;
    }

    public void setKeyPoint(String keyPoint) {
        this.keyPoint = keyPoint;
    }

    public String getWorkOvertime() {
        return workOvertime;
    }

    public void setWorkOvertime(String workOvertime) {
        this.workOvertime = workOvertime;
    }

    public String getNote() {
        return note;
    }

    public void setNote(String note) {
        this.note = note;
    }
}
